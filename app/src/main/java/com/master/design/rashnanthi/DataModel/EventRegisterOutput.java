package com.master.design.rashnanthi.DataModel;

public class EventRegisterOutput {
    private String creatorcoach;
    private String success;
    private String countrycode;
    private String mobile;
    private String otp;
    private String message;
    private String userid;


    public String getCreatorcoach() {

        return creatorcoach;

    }

    public void setCreatorcoach(String creatorcoach) {

        this.creatorcoach = creatorcoach;

    }

    public String getSuccess() {

        return success;

    }

    public void setSuccess(String success) {

        this.success = success;

    }

    public String getCountrycode() {

        return countrycode;

    }

    public void setCountrycode(String countrycode) {

        this.countrycode = countrycode;

    }

    public String getMobile() {

        return mobile;

    }

    public void setMobile(String mobile) {

        this.mobile = mobile;

    }

    public String getOtp() {

        return otp;

    }

    public void setOtp(String otp) {

        this.otp = otp;

    }

    public String getMessage() {

        return message;

    }

    public void setMessage(String message) {

        this.message = message;

    }

    public String getUserid() {

        return userid;

    }

    public void setUserid(String userid) {

        this.userid = userid;

    }
}
